            <h3 class="backColor">Que es?</h3>
            <p class="backColor">
                La ecología es la especialidad científica centrada en el estudio y análisis del vínculo que surge entre
                los seres vivos y el entorno que los rodea, entendido como la combinación de los factores abióticos
                (entre los cuales se puede mencionar al clima y a la geología) y los factores bióticos (organismos que
                comparten el hábitat). La ecología analiza también la distribución y la cantidad de organismos vivos
                como resultado de la citada relación.
            </p>

            <div class="imagen">
                <img src="./assets/img/ecologia/ecologia.jpg" alt="Ecologia" width="500">
            </div>

            <p class="backColor">
                La ecología es la rama de la biología que se dedica al estudio de los seres vivos y la relación de éstos
                con el medio ambiente en el que habitan. Además, la ecología estudia la abundancia y distribución de
                seres vivos que existe en un área o región determinada.
            </p>

            <h2 id="reciclaje">Reciclaje</h2>

            <h3 class="backColor">Que es?</h3>
            <p class="backColor">
                El reciclaje es el proceso que consiste en recolectar y transformar los residuos o materiales de desecho
                en nuevas materias o productos para su reutilización.
            </p>

            <p class="backColor">
                Este proceso consta de diferentes pasos como la recolección de materiales de desecho o basura, su
                clasificación (vidrio, plástico, aluminio, papel, madera, desechos biodegradables, etc.), su
                procesamiento y transformación en nuevas materias primas o productos que tendrán un nuevo ciclo de vida
                útil y, que posiblemente, se podrán volver a reciclar.
            </p>

            <p class="backColor">
                En el reciclaje participan las plantas de clasificación, donde son separados los residuos, y las plantas
                recicladoras, donde los residuos son trasformados para su aprovechamiento.
            </p>

            <div class="imagen">
                <img src="./assets/img/ecologia/juego-clasificacion-basura_74855-15415.jpg" alt="reciclaje" width="500">
            </div>

