            <h2 id="huella">Huella ecologica</h2>

            <div class="imagen">
                <img src="./assets/img/ecologia/huella.jpg" alt="huella " width="500">
            </div>
            <h3 class="backColor">Que es?</h3>
            <p class="backColor">

                La huella ecológica es un indicador que mide el impacto ambiental generado por la demanda que hacemos de
                los recursos naturales , considerando el área de terreno y agua necesarios para producir los bienes y
                servicios que consumimos y para absorber los residuos que generamos. Es una medida de la cantidad de
                recursos naturales necesarios para mantener nuestro estilo de vida y actividades cotidianas. La huella
                ecológica se mide en hectáreas globales (hag) por habitante y año , y es utilizada para evaluar la
                sostenibilidad de nuestras actividades y consumo de recursos. La huella ecológica es un reflejo de la
                cantidad de recursos naturales que consumimos en nuestras actividades diarias y es importante reducirla
                para disminuir nuestro impacto en el medio ambiente y garantizar la sostenibilidad de nuestros sistemas
                naturales.
            </p>

