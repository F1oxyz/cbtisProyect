            <h3 class="backColor">Que es?</h3>
            <p class="backColor">
                La reforestación consiste en repoblar zonas deforestadas para recuperar bosques destruidos en el pasado
                reciente. Ante la pérdida de grandes masas forestales, vitales para la bsorción de CO2, la generación
                de oxígeno y la lucha contra el cambio climático, se hace necesaria la plantación masiva de árboles
                nuevos con el objetivo de evitar la pérdida de ecosistemas y frenar el deterioro del planeta.
            </p>
            <p class="backColor">
                La reforestación, por tanto, contribuye a la consecución de diversos objetivos, todos ellos encaminados
                a recuperar la estabilidad que proporcionan los bosques.
